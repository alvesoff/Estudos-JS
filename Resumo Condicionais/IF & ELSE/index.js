//* O uso do IF e ELSE é fundamental para controlar o fluxo de um codigo com base em condiçoes especificas.
//* Exemplo simples de uso do IF e ELSE em JavaScript */

let gustavoIdade;

gustavoIdade = 17;

if (gustavoIdade === 18) {
  console.log("Gustavo acabou de se tornar adulto!");
} else if (gustavoIdade >= 19) {
  console.log("Gustavo ja é adulto.");
} else {
  console.log("Gustavo ainda é menor de idade.");
}
